/**
 * Comparison operators
 *
 * === strict equality
 * !== strict inequality
 * > greater than
 * < less than
 * >= greater than or equal
 * <= less than or equal
 * 
 * ----------------------
 * DON'T USE THESE 2:
 * * == equality (we don't use this)
 * != inequality (we don't use this)
 */

console.log("is 2 euqal to 2?", 2 === 2);
console.log("is 5 euqal to 2?", 5 === 2);

console.log("is 2 NOT equal to 2?", 2 !== 2);
console.log("is 5 NOT equal to 2?", 5 !== 2);

console.log(`is "hello" equal to "hello"?`, "hello" === "hello");
console.log(`is "hello" NOT equal to "hello"?`, "hello" !== "hello");

console.log(`is "" equal to ""?`, "" === "");

console.log(`is "2" equal to 2?`, "2" === 2);

// console.log(`is "2" equal to 2?`, "2" == 2);

console.log(`is 2 greater than 5?`, 2 > 5);
console.log(`is 100 greater than 25?`, 100 > 25);

console.log(`is 2 less than 5?`, 2 < 5);
console.log(`is 100 less than 25?`, 100 < 25);

console.log(`is "a" less than "b"?`, "a" < "b");
console.log(`is "a" less than "B"?`, "a" < "B");

console.log(`is "_hello" less than "hey"?`, "_hello" < "hey");

console.log(`is 2 greater than 2?`, 2 > 2);

console.log(`is 2 greater than or equal to 2?`, 2 >= 2);
console.log(`is 2 less than or equal to 2?`, 2 <= 2);

console.log(`is -10 less than -5?`, -10 < -5);
console.log(`is 1.523 less than 1.6?`, 1.523 < 1.6);

