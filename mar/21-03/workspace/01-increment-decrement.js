let counter = 0;

/**
 * The ++ operator does 2 things:
 * 
 * - it increases a variable by 1
 * - it reassigns the new value to that variable
 * 
 * It's a shortcut for
 * 
 * counter += 1;
 * 
 */

counter++;
console.log(counter);

counter++;
console.log(counter);

counter++;
console.log(counter);

counter--;
console.log(counter); // 2

