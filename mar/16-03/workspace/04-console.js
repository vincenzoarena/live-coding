/**
 * We can use console.log() to output anything we want to the console
 *
 */

// we can also output more than 1 value
// or more than 1 variable at the same time
// simply separate them by a comma

console.log("hello", 1, 2, 3, true, false);

console.log("hello", "world");

let firstName = "carlo";
let city = "berlin";

console.log(firstName, city);

console.log(firstName, "lives in", "\n", city);