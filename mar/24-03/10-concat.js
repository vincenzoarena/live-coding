/**
 * concat()
 * 
 * With the concat() method we can merge 2 or more arrays.
 * The method gives us back a new array and the original arrays are not changed
 * 
 */

let array1 = [1, 2, 3];
let array2 = [4, 5, 6];

// let's merge the 2 arrays into a new array
let array3 = array1.concat(array2);

console.log(array1);
console.log(array2);
console.log(array3);
