/**
 * Accumulator pattern
 * 
 * - we have a variable (an accumulator) initialized to some value, normally:
 *  - 0, if we want a number as result
 *  - "", if we want a string as result
 *  - [], if we want an array as result
 * - we have a loop that goes through a string, an array, 
 * or simply generates list of numbers
 * - we fill the accumulator variable from withing the loop
 * - we return the accumulator variable
 */